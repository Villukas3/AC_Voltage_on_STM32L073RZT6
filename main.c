/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  lcd1.hi2c = &hi2c1;
  lcd1.address = 0x4E;
  lcd_init(&lcd1);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint32_t lastUartTime = 0;
  uint32_t lastLcdTime = 0;

  while (1)
  {
    HAL_ADC_Start(&hadc);
    if (HAL_ADC_PollForConversion(&hadc, 20) != HAL_OK) {
      Error_Handler();
    }

    readValue = HAL_ADC_GetValue(&hadc);

    //Uzdedamos ribos, kad butu gaunamas sklandus veikimas, nes taip isvengiama triuksmingo input matuojant
    //ir galima matyti triuksmus, kai irenginys atjungtas nuo AC Å¡altinio
    if (readValue >= 100 || readValue == 0) {
      voltage = (float)((readValue * ref_voltage_range) / ADC_range);
      real_volt = voltage * ((11000.0f + 4700.0f) / 4700.0f);

      if (numSamples < MAX_SAMPLES) {
        voltageArray[numSamples++] = real_volt;
      } else {
        numSamples = 0;
      }

      //amp = amplitude todel ieskoma max verte
      voltamp = findMaxVoltage();
      //rms buvo galima rasti dviems budais:
      //randant tikra rms arba paimant momentini rms apskaiciavus nuo peak
      voltrms = voltamp * 0.707f;
    }

    // i UART sasaja duomenys paduodami kas 200 ms
    if (HAL_GetTick() - lastUartTime >= 200) {
      lastUartTime = HAL_GetTick();
      ValueToTerminal();
    }

    // i LCD ekraniuka duomenys paduodami kas 500 ms
    if (HAL_GetTick() - lastLcdTime >= 500) {
      lastLcdTime = HAL_GetTick();
      // naudojant lcd_gotoxy(0,0) vietoje lcd_clear sutaupo laiko ir isvengiama mirksejimo
      lcd_gotoxy(&lcd1, 0, 0);
      sprintf(buffer, "Umax= %.2f V", voltamp);
      lcd_puts(&lcd1, buffer);

      lcd_gotoxy(&lcd1, 0, 1);
      sprintf(buffer2, "Urms= %.2f V", voltrms);
      lcd_puts(&lcd1, buffer2);
    }
    HAL_Delay(10); // maza pertrauktis siekent isvengti 100% CPU panaudojimo
  }
  /* USER CODE END 3 */
}


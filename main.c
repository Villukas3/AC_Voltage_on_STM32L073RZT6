float findMaxVoltage(void)
{
    float maxVoltage = voltageArray[0];
    for (int i = 1; i < numSamples; i++)
    {
        if (voltageArray[i] > maxVoltage)
        {
            maxVoltage = voltageArray[i];
        }
    }
    return maxVoltage;
}
void ValueToTerminal(){
	//naudojant PuTTY jam reikia speed is uart kas yra 115200 ir
	//STM32 porto kas siuo atveju yra port 5
	sprintf(buffer, "Umax= %.2fV \r\n", voltamp);
	HAL_UART_Transmit(&huart2, (uint8_t *)buffer, strlen(buffer), HAL_MAX_DELAY);
	sprintf(buffer2, "Urms= %.2fV \r\n", voltrms);
	HAL_UART_Transmit(&huart2, (uint8_t *)buffer2, strlen(buffer2), HAL_MAX_DELAY);
	HAL_UART_Transmit(&huart2, (uint8_t *)"\r\n", 2, HAL_MAX_DELAY);
}
